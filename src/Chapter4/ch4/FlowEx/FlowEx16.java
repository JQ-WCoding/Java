package Chapter4.ch4.FlowEx;

public class FlowEx16 {
    public static void main(String[] args) {
        // 별로 사각형 만들기
        for (int i = 0; i < 5; i++) {
            for (int j = 0; j < 10; j++) {
                System.out.print("*");
            }
            System.out.println();
        }
    }
}
