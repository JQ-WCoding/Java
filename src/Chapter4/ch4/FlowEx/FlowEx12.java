package Chapter4.ch4.FlowEx;

public class FlowEx12 {
    public static void main(String[] args) {
        // 0 ~ 4까지 모든 숫자를 출력
        for (int i = 0; i < 5; i++) {
            System.out.println(i);
        }
        // 1 ~ 4까지 모든 숫자를 출력
        for (int i = 1; i < 5; i++) {
            System.out.print(i);
        }

    }
}
